using System;

namespace Loops2
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int i = 0; i < 10; i++)
            {
                Console.WriteLine(i + 1);
            }


            for (int i = 10; i > 0; i--) //i-- i = i-1
            {
                Console.WriteLine(i);
            }

        }
    }
}
